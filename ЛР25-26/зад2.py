import numpy as np# Импорт библиотеки NumPy под псевдонимом np

# Создание одномерного массива из 12 последовательных целых чисел от 12
A = np.arange(12, 24)# Создание массива от 12 до 23 (не включая 24)
print("Одномерный массив A:")
print(A)

# Преобразование одномерного массива A в двумерные массивы различных форм
matrix_3x4 = A.reshape(3, 4)# Преобразование одномерного массива A в матрицу размером 3x4
matrix_4x3 = A.reshape(4, 3)# Преобразование одномерного массива A в матрицу размером 4x3
matrix_2x6 = A.reshape(2, 6)# Преобразование одномерного массива A в матрицу размером 2x6

print("\nДвумерные массивы разной формы:")# Вывод заголовка для двумерных массивов различных форм
print("Матрица 3x4:")
print(matrix_3x4)# Вывод матрицы 3x4
print("\nМатрица 4x3:")
print(matrix_4x3)# Вывод матрицы 4x3
print("\nМатрица 2x6:")
print(matrix_2x6)# Вывод матрицы 2x6